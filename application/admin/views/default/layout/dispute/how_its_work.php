<h2>Dispute Cases : </h2>
<br />
<br />

<p><b>A) Take Poster Win : </b> If Task Poster Win the dispute at that Administrator have <b>2 option</b><br />

<ul style="padding:0px 0px 0px 10px;">
<li>1) Take the refund fees(which is in task setting)</li>
<li>2) Make 100% payment back </li>
</ul>

</p>


<br />
<br />

<p><b>B) Worker Win : </b> If Worker Win the dispute at that Administrator Take the commission(which is in task setting) on total Task Price and remaining amount added to worker account.</p>



<br />
<br />

<p><b>C) Partial Payment : </b> Task Poster and Worker <b>both win</b> this case. Administrator can set any amount for both user but must not exceed from total Task Price.

<ul style="padding:0px 0px 0px 10px;">
<li>1)<b>Worker :</b> Administrator Take the commission(which is in task setting) on total Task Price and remaining amount added to worker account.</li>
<li>&nbsp;</li>
<li>2)<b>Task Poster :</b> Administrator have <b>2 option</b> for this user.


<ul style="padding:0px 0px 0px 10px;">
<li>&nbsp;</li>
<li>i) Take the refund fees(which is in task setting) on added amount</li>
<li>ii) Make Total payment of added amount </li>
</ul>


</li>

</ul>

</p>




<br />
<br />

<p><b>D) Resumed Task : </b> Administrator can <b>restart</b> the Task.</p>




