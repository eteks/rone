        function sub_contact()
        {
        
                if($('#company_name').val()=="")
                {
                    alert('Ange företagsnamn');
                    return false;
                }
                if($('#contact_name').val()=="")
                {
                    alert('Ange kontaktperson');
                    return false;
                }
                if($('#contact_email').val()=="")
                {
                    alert('Vänligen ange email');
                    return false;
                }
                var email=$('#contact_email').val();
                var mailformat = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/;
